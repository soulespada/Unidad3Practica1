package com.ieschabas;
/**
 * Clase que devuelve el mayor entre dos numeros
 * @author Adrian
 * @version 1.0
 */
public class Mayores {
    /**
     * Metodo que devuelve el mayor entre dos numeros
     * @param a
     * @param b
     * @return
     */
    public int max2(int a, int b){
        if(a>b){
            return a;
        }else{return b;}
    }
}
